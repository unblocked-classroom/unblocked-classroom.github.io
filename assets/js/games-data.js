// Utility functions
function getGamesByFilter(filter) {
    switch (filter) {
        case 'popular':
            return gamesData.filter(game => game.popular);
        case 'new':
            return gamesData.filter(game => game.new);
        case 'trending':
            return gamesData.filter(game => game.trending);
        default:
            return gamesData;
    }
}

function getGamesByCategory(category) {
    return gamesData.filter(game => game.category === category);
}

function searchGames(query) {
    const searchTerm = query.toLowerCase();
    return gamesData.filter(game => 
        game.title.toLowerCase().includes(searchTerm) ||
        game.category.toLowerCase().includes(searchTerm)
    );
}

function getRandomGames(excludeGame, count = 6) {
    const otherGames = gamesData.filter(g => g.title !== excludeGame.title);
    const shuffled = [...otherGames].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

function createGameSlug(title) {
    return title.toLowerCase()
        .replace(/[^a-z0-9\s-]/g, '')
        .replace(/\s+/g, '-')
        .replace(/-{2,}/g, '-')
        .replace(/^-|-$/g, '');
}